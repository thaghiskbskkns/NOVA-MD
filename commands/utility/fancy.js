// Function to convert text to a specific style
function convertToStyle(text, styleMap) {
    let result = '';
    for (let char of text) {
        if (styleMap[char]) {
            result += styleMap[char];
        } else {
            result += char;
        }
    }
    return result;
}

// Get all available styles
function getAllStyles(text) {
    const styles = [];
    
    for (const [styleName, styleMap] of Object.entries(styleMaps)) {
        const converted = convertToStyle(text, styleMap.chars);
        if (converted !== text) {
            styles.push({
                name: styleName,
                result: converted
            });
        }
    }
    
    return styles;
}

